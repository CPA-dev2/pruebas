/**
 * Función para obtener municipios de un departamento específico
 * @param {string} departamento - Nombre del departamento
 * @returns {Array<string>} - Array de municipios del departamento
 */
export const getMunicipiosByDepartamento = (departamento) => {
  return MUNICIPIOS_POR_DEPARTAMENTO[departamento] || [];
};

/**
 * Función para verificar si un municipio pertenece a un departamento
 * @param {string} departamento - Nombre del departamento
 * @param {string} municipio - Nombre del municipio
 * @returns {boolean} - true si el municipio pertenece al departamento
 */
export const validateMunicipioInDepartamento = (departamento, municipio) => {
  const municipios = getMunicipiosByDepartamento(departamento);
  return municipios.includes(municipio);
};

/**
 * Función para buscar municipios por nombre (búsqueda parcial)
 * @param {string} searchTerm - Término de búsqueda
 * @returns {Array<Object>} - Array de objetos con departamento y municipio
 */
export const searchMunicipios = (searchTerm) => {
  const results = [];
  const term = searchTerm.toLowerCase();
  
  Object.entries(MUNICIPIOS_POR_DEPARTAMENTO).forEach(([departamento, municipios]) => {
    municipios.forEach(municipio => {
      if (municipio.toLowerCase().includes(term)) {
        results.push({ departamento, municipio });
      }
    });
  });
  
  return results;
};

/**
 * Función para obtener todos los municipios de Guatemala (plano)
 * @returns {Array<string>} - Array con todos los municipios
 */
export const getAllMunicipios = () => {
  return Object.values(MUNICIPIOS_POR_DEPARTAMENTO).flat();
};